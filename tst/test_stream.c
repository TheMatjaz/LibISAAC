static void test_stream_with_zero_seed(void)
{
    isaac_ctx_t ctx;
    isaac_init(&ctx, NULL, 0);  // Zero seed
    isaac_uint_t stream[300] = {0};

    isaac_stream(&ctx, stream, 300);

    atto_neq(stream[0], 0);
    atto_memeq(stream, expected_stream_with_zero_seed, 300 * sizeof(isaac_uint_t));
    atto_eq(ctx.next_index, 256 * 2 - 300 - 1);
}

static void test_stream_with_zero_seed_multiple_calls(void)
{
    isaac_ctx_t ctx;
    isaac_init(&ctx, NULL, 0);  // Zero seed
    isaac_uint_t stream[300] = {0};

    for (uint16_t i = 0; i < 100; i++)
    {
        isaac_stream(&ctx, &stream[3 * i], 3);
    }

    atto_neq(stream[0], 0);
    atto_memeq(stream, expected_stream_with_zero_seed, 300 * sizeof(isaac_uint_t));
    atto_eq(ctx.next_index, 256 * 2 - 300 - 1);
}

