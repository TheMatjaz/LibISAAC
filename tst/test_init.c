{
    isaac_ctx_t ctx;
    const uint8_t seed[8] = {1, 2, 3, 4, 5, 6, 7, 8};

    isaac_init(&ctx, seed, 8);

    atto_eq(sizeof(ctx), sizeof(nonzero_initialised_ctx));
    atto_memeq(&ctx, &nonzero_initialised_ctx, sizeof(ctx));
}

